% Να γράψετε πρόγραμμα το οποίο να υπολογίζει τη συνάρτηση f(Χ). Το πρόγραμμα σας
% να ζητάει τη τιμή του X και να επιστρέφει το f(X), στη συνέχεια να εκτυπώνει το X και
% το f(X). Η συνάρτηση f(Χ) ορίζεται στο σχήμα, Σχήμα 1, Να χρησιμοποιήσετε αποκοπή
% στις προτάσεις του προγράμματός σας όπου χρειάζεται.
%       { 0 εάν Χ < 0
% f(Χ) ={ X εάν 0 ≤ Χ ≤ 1
%       { 1 εάν Χ > 1

f(X, X) :- X >= 0, X =< 1, !.
f(X, 0) :- X < 0, !.
f(X, 1) :- X > 1, !.